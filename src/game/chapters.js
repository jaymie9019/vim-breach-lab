export function getChapterDoc(chapter) {
  return chapterDocs[chapter] ?? null;
}

export function getChapterLevels(chapter) {
  return levels.filter((level) => level.chapter === chapter);
}

export function isFirstLevelOfChapter(levelIndex) {
  if (levelIndex <= 0) {
    return true;
  }

  return levels[levelIndex - 1].chapter !== levels[levelIndex].chapter;
}

export function isLastLevelOfChapter(levelIndex) {
  if (levelIndex >= levels.length - 1) {
    return true;
  }

  return levels[levelIndex + 1].chapter !== levels[levelIndex].chapter;
}
