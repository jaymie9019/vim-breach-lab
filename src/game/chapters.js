import { levels } from "./levels.js";

function buildOrderedChapters() {
  const seen = new Set();
  const ordered = [];

  for (const level of levels) {
    if (seen.has(level.chapter)) {
      continue;
    }

    seen.add(level.chapter);
    ordered.push(level.chapter);
  }

  return ordered;
}

